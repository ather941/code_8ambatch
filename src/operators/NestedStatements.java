package operators;

public class NestedStatements {
	public static void main(String[] args) {
		//  if with in if
		
		// loops with loop
		
		
		for(int i = 0; i < 10 ;i++) { // 10
			for(int j = 0; j < i; j++) { // 10
				System.out.print("*"); // ln= newline
			}
			System.out.println();  // new line in console
		}
		
	}
}
